        public void DrawBoard()
        {
            int indexOfColumn = 3;
            int indexOfRow = 2;
            int cell = 0;
            ConsoleColor[] colors = (ConsoleColor[])ConsoleColor.GetValues(typeof(ConsoleColor));
            Console.BackgroundColor = colors[0];
            for (var i = 0; i < _rows; i++)
            {
                for (var j = 0; j < _columns; j++)
                {
                    if (i == indexOfRow && j == indexOfColumn)
                    {
                        if (_board.BoardCells[cell].CellValue > 0)
                        {
                            Console.ForegroundColor = colors[11];
                            Console.Write(_board.BoardCells[cell].CellValue);
                        }
                        else
                        {
                            Console.Write(" ");
                        }
                        cell++;
                        indexOfColumn += 4;
                    }
                    else
                    {
                        Console.ForegroundColor = colors[15];
                        Console.Write(boardData[i, j]);
                    }
                }
                if (i == indexOfRow)
                {
                    indexOfColumn = 3;
                    indexOfRow += 2;
                }
                Console.WriteLine();
            }
            Console.WriteLine();
        }
